-module(agent_config).
-export([get/1]).

%% ===================================================================
%% API functions
%% ===================================================================

get(Id) ->
	proplists:get_value(Id, get_configs()).

%% ===================================================================
%% Local Functions
%% ===================================================================

get_configs() ->
	{ok, Configs} = file:consult("../agent.conf"),
	Configs.
	
